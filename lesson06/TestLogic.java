package lesson06;

public class TestLogic {
    public static void main(String[] args) {
        int x=3,y=6;
        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println((x==3)&&(y!=6));
        System.out.println((x==3)||(y!=6));
        System.out.println(!(x!=y));
    }
}
